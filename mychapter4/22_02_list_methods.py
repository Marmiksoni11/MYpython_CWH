#LIST METHODS

#l.sort() asceding order



l = [22,3,12,1,90,11]
l.sort()
print(l)

#l.reverse() descending order

l.reverse()
print(l)

# l.append() adds at the end of list 

l.append(33)
print(l)

# l.insert(0,22) here 0 is the index followed by the value. it adds the values at
# the given index\
l.insert(0,80)
print(l)

#l.pop () removes element from given index

l.pop(0)
print(l)

#l.remove removes given element in the list
l.remove(33)
print(l)